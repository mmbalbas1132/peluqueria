            <h2 class="card-title">Castañal, la peluquería que vino de Nueva York</h2>
            <p class="card-text" style="text-align: justify">La histórica peluquería lucense Castañal cumplirá 50 años
                en la calle Conde Pallares este 25 de agosto. La fecha también coincidirá con su 90º aniversario desde
                su llegada a la ciudad.<br><span class="extra-info" style="display: none">
                    Luis López, conocido en la ciudad como Luis Castañal, es el actual propietario de la peluquería,
                    aunque la historia de este pequeño negocio familiar comenzó a principios del siglo XX, cuando el tío
                    de Luis, Antonio Castañal empezó a profesionalizarse en el oficio de la peluquería en las ciudades
                    americanas de Nueva York y Chicago.<br>La familia de Antonio Castañal fue una de las primeras
                    gallegas en emigrar a Norteamérica. "De aquellas se les miraba como a los caballos, tenían que hacer
                    largas cuarentenas al llegar al país y eran mal vistos por las altas clases" comenta Luis acerca de
                    la historia que vivieron sus familiares y tantos emigrantes a EE.UU.<br>Pero entró y Antonio
                    encontró trabajo en diversos oficios del continente americano. Trabajó en hornos de fundición y en
                    fábricas, hasta que aprendió el oficio de la peluquería en Chicago. Poco después emprendió su propio
                    negocio en Nueva York, hasta que en 1932 pudo regresar a Lugo y establecer la peluquería Castañal en
                    su tierra.<br>En ese año, además de instalar el negocio al lado de la Puerta de la Estación, "que
                    por muchos años se conocería como Ronda de los Caídos", comenta Luis. Antonio comenzó a ser lector
                    habitual de El Progreso, una tradición que hizo que la peluquería Castañal se convierta en uno de
                    los establecimientos más antiguos suscritos a este diario.<br>Posteriormente, el 25 de agosto de
                    1972, el negocio movió su ubicación a la calle Conde Pallares, donde continúa ofreciendo sus
                    servicios "al estilo tradicional".<br>UN LOCAL HISTÓRICO. <br>Luis Castañal cuenta con emoción
                    la historia de su negocio familiar y recuerda algunas de las etapas más destacadas que marcaron la
                    peluquería: "Cuando abrimos en Conde Pallares y en los años gloriosos posteriores, contábamos con
                    cuatro peluqueras y un aprendiz". Hoy el negocio cuenta con una nueva gerente que lleva años en el
                    negocio, la peluquera Elvira Carracedo.<br>Asimismo, Luis Castañal asegura que esta histórica
                    peluquería fue pionera en introducir muchos de los cortes en mujeres que se comenzaban a poner de
                    moda en la primera mitad del siglo XX, como el famoso garçon.<br>El actual propietario continuó con
                    el negocio en honor a su tío, trabajando también en el propio oficio. Sin embargo, el lucense
                    también es reconocido en la ciudad por dedicarse a los negocios automovilísticos, que llevó a
                    Madrid, y por ser antiguo presidente del Club Ciclista San Froilán.<br>"Hoy recuerdo todo el trabajo
                    y sacrificio de mi tío en mantener este negocio familiar y aún conservo en su memoria el llavero con
                    el nombre de la calle neoyorquina y lucense de la peluquería" dice.</span></p>
